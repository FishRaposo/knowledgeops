"""Markdown report generator for evaluation results."""

from typing import Any

from fastapi import APIRouter, HTTPException

from app.runners.rag_runner import _eval_runs

router = APIRouter(prefix="/eval")


@router.get("/runs/{run_id}/report")
async def get_run_report(run_id: str) -> dict[str, str]:
    """Generate a markdown report for an evaluation run.

    Args:
        run_id: Evaluation run identifier.

    Returns:
        Markdown-formatted report string.
    """
    run_data = _eval_runs.get(run_id)
    if not run_data:
        raise HTTPException(status_code=404, detail=f"Run {run_id} not found")

    report = generate_markdown_report(run_data)
    return {"report": report, "run_id": run_id}


def generate_markdown_report(run_data: dict[str, Any]) -> str:
    """Generate a markdown report from evaluation run data.

    Args:
        run_data: Evaluation run data with results.

    Returns:
        Formatted markdown report string.
    """
    results = run_data.get("results", [])
    name = run_data.get("name", "Unknown Run")

    lines = [
        f"# Evaluation Report: {name}",
        "",
        "## Summary",
        "",
        f"- **Total test cases:** {len(results)}",
        f"- **Status:** {run_data.get('status', 'unknown')}",
        "",
    ]

    if results:
        lines.append("| Query | Semantic | Citation | Refusal |")
        lines.append("|-------|----------|----------|---------|")
        for r in results:
            scores = r.get("scores", {})
            lines.append(
                f"| {r.get('query', '')[:40]}... "
                f"| {scores.get('semantic_match', 'N/A')} "
                f"| {scores.get('citation_check', 'N/A')} "
                f"| {scores.get('refusal_check', 'N/A')} |"
            )

    lines.extend(["", "---", "*Generated by KnowledgeOps Eval Service*"])
    return "\n".join(lines)
